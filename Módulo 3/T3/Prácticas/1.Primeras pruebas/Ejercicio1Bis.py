
e =  15 # Número de factorial.
resultado = 1 # Cálculo del factorial.
total = 1 # Sumatorio.
i = 1
while i <= e:
        resultado = resultado * i # Cáculo del factorial (la multiplicación !)
        total = total + 1/resultado  # La suma de los elementos VALOR DE e
        i = i + 1 # Siempre hay que sumar +1 esta fórmula
        print(total)